            UserID = user;
            Salt = salt;
            Hash = hash;
            Key = key;
            ValidKey = validKey;
        }

        public User(string user, string salt, string hash, bool validKey = false)
        {
            UserID = user;
            Salt = salt;
            Hash = hash;
            ValidKey = validKey;
        }

        public User(bool validKey = false)
        {
            ValidKey = validKey;
        }

        /*=================================================================================================
		PUBLIC METHODS
		*================================================================================================*/
        /*************************************************************************************************/
        public string GetUserString()
        {
            string userString = "";

            userString = string.Format("{0},{1},{2}", UserID, Salt, Hash);

            return userString;
        }

